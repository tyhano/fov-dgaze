    # these are the 3 coarse layers generated by masking; their folder names depend on pooling sizes.
    # We infer them the same way as compose_models.py did:
    sqrt_m = args.max_pooling_size ** 0.5
    interval = (sqrt_m - 1) / (L - 1)
    psizes = []
    for i in range(L):
        pooling = 1 + interval * i
        psizes.append(round(pooling**2))
    # fixed args used in repo scripts
    arg2, arg3 = 6000, 1500
    layer_paths = []
    for li in range(1, L):
        folder = f"{psizes[li]}_{arg2}-{arg3}_{L}_{args.metric}"
        p = os.path.join(args.base_folder, folder, "point_cloud", f"iteration_{args.coarse_iter}", "point_cloud.ply")
