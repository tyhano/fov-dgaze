from plyfile import PlyData, PlyElement

# ---------- helper: read PLY vertex to numpy structured ----------
def read_ply(path):
    ply = PlyData.read(path)
    v = ply['vertex'].data
    return v

def write_ply(path, v_np):
    el = PlyElement.describe(v_np, 'vertex')
    PlyData([el], text=False, byte_order='<').write(path)

def get_fields(v):
    return v.dtype.names

# ---------- build DC/opacity tables aligned to PS1 using index layers ----------
def build_tables(ps1_v, layer_paths):
    """
    ps1_v: structured np array for PS1, length N
    layer_paths: list of (layer_id, ply_path_with_index) for layers 1..L-1
    Returns:
      shs_dcs: np float32 [N, L, 3]
      opac:   np float32 [N, L]
    """
    N = len(ps1_v)
    # fields exist in PS1
    shs_dcs = np.zeros((N, L, 3), dtype=np.float32)
    opac = np.ones((N, L), dtype=np.float32)

    # layer0 from PS1
    shs_dcs[:,0,0] = ps1_v['f_dc_0'].astype(np.float32)
    shs_dcs[:,0,1] = ps1_v['f_dc_1'].astype(np.float32)
    shs_dcs[:,0,2] = ps1_v['f_dc_2'].astype(np.float32)
    opac[:,0] = ps1_v['opacity'].astype(np.float32)

    # propagate
    for li in range(1, L):
        shs_dcs[:,li,:] = shs_dcs[:,li-1,:]
        opac[:,li] = opac[:,li-1]

    # overlay from each coarse ply using its index field
    for li, p in layer_paths:
        v = read_ply(p)
        if 'index' not in v.dtype.names:
            raise RuntimeError(f"{p} has no 'index' field (need index-version ply).")
        idx = v['index'].astype(np.int64)
        # update DC + opacity for those points at layer li
        shs_dcs[idx, li, 0] = v['f_dc_0'].astype(np.float32)
        shs_dcs[idx, li, 1] = v['f_dc_1'].astype(np.float32)
        shs_dcs[idx, li, 2] = v['f_dc_2'].astype(np.float32)
        opac[idx, li] = v['opacity'].astype(np.float32)

    return shs_dcs, opac

# ---------- project 3D points to screen uv using a reference camera ----------
