def load_cameras_json(path):
    import json
    with open(path, "r") as f:
        data = json.load(f)

    cam = data[0] if isinstance(data, list) else (data["cameras"][0] if isinstance(data, dict) and "cameras" in data else data)

    # expected keys in your cameras.json: width, height, position, rotation, fx, fy
    W = int(cam["width"])
    H = int(cam["height"])
    fx = float(cam["fx"])
    fy = float(cam["fy"])

    # principal point (assume centered)
    cx = (W - 1) * 0.5
    cy = (H - 1) * 0.5

    # camera center in world
    C = np.array(cam["position"], dtype=np.float32).reshape(3)

    # rotation: assume a 3x3 matrix (row-major) that maps camera->world
    R = np.array(cam["rotation"], dtype=np.float32).reshape(3,3)

    return W, H, fx, fy, cx, cy, R, C


def project_uv(xyz, R, C, fx, fy, cx, cy, W, H):
    # world -> camera: Xc = R^T (X - C)   (R is camera->world)
    X = xyz.astype(np.float32)
    Xc = (X - C[None, :]) @ R  # (N,3) because (X-C) * (camera->world)?? we need R^T; so use @R.T if R is camera->world.
    # Correction: if R is camera->world, then world->camera is R^T
    Xc = (X - C[None, :]) @ R.T

    z = Xc[:, 2]
    valid = z > 1e-6

    u = fx * (Xc[:, 0] / np.maximum(z, 1e-6)) + cx
    v = fy * (Xc[:, 1] / np.maximum(z, 1e-6)) + cy

    # convert to 0..1 uv, and flip v to match screen-space convention
    uu = (u / max(W-1, 1)) 
    vv = 1.0 - (v / max(H-1, 1))

    uv = np.stack([uu, vv], axis=1).astype(np.float32)
    # also require inside screen
    valid = valid & (uu >= 0) & (uu <= 1) & (vv >= 0) & (vv <= 1)
    return uv, valid

