        layer_sel[d < args.r1] = 1
        layer_sel[d < args.r0] = 0

        # build output vertex array (start from PS1 geometry)
        dtype = []
        for k in keep:
            if k in fields and k != "opacity":
                dtype.append((k, ps1_v.dtype.fields[k][0]))
            elif k in ["nx","ny","nz"]:
                dtype.append((k, 'f4'))
            else:
                dtype.append((k, 'f4'))
        out_v = np.empty(len(ps1_v), dtype=dtype)

        # copy everything from PS1 for shared fields
        for k in keep:
            if k in fields and k not in ["f_dc_0","f_dc_1","f_dc_2","opacity"]:
                out_v[k] = ps1_v[k]
            elif k in ["nx","ny","nz"]:
                out_v[k] = ps1_v[k].astype(np.float32) if has_n else 0.0
            elif k.startswith("f_rest_"):
                out_v[k] = ps1_v[k] if k in fields else 0.0

        # overwrite DC + opacity from selected layer tables
        out_v["f_dc_0"] = shs_dcs[np.arange(len(ps1_v)), layer_sel, 0]
